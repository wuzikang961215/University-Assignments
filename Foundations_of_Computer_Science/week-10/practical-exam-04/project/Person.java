public abstract class Person{
	protected String name;
	protected int age;
	
	Person(){
		this.name = "unnamed";
		this.age = 0;
	}

	/* implement accessors and mutators */
	// accessors
	public String getName(){
		return this.name;
	}
	public int getAge(){
		return this.age;
	}
	// mutators
	public void setName(String name){
		this.name = name;
	}
	public void setAge(int age){
		this.age = age;
	}

}