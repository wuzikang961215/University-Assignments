class Problem{
    public static void main(String [] args){
        int number = 0;
        for(int i=1; i<=40; i++){
            number++;
            if(number >= 1 && number < 10){
                for(int x=1; x<=number; x++){
                    System.out.print("*");
                }
            }
            else if(number >= 10 && number < 20){
                for(int x=1; x<=number; x++){
                    System.out.print("=");
                }
            }
            else if(number >= 20 && number < 30){
                for(int x=1; x<=number; x++){
                    System.out.print("x");
                }
            }
            else if(number >= 30 && number <= 40){
                for(int x=1; x<=number; x++){
                    System.out.print("o");
                }
            }
            System.out.print("\n");
        }
    }
}