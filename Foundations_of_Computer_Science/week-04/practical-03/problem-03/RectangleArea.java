class RectangleArea extends Rectangle{
    // basic constructor
    public RectangleArea(){
        
    }

    // parametric constructor
    public RectangleArea(int width, int height){
        super(width,height);
    }

    // reading the value of width and height from user input
    public int[] read_input(int width, int height){
        int [] rectangle = {width,height};
        return rectangle;
    }
    
    // printing the area of the rectangle
    public void display(int width, int height){
        Rectangle rectangle = new Rectangle(width,height);
        rectangle.display(width, height);
        int rectangleArea = width*height;
        System.out.println(rectangleArea);
    }
}