class Rectangle{
    private int width;
    private int height;

    // basic constructor
    public Rectangle(){
        
    }

    // parametric constructor
    public Rectangle(int width, int height){
        this.width = width;
        this.height = height;
    }

    // accessors
    public int get_width(){
        return this.width;
    }
    public int get_height(){
        return this.height;
    }

    // mutators
    public void set_width(int width){
        this.width = width;
    }
    public void set_height(int height){
        this.height = height;
    }

    // printing the width and height of the rectangle
    public void display(int width, int height){
        System.out.println(width+" "+height);
    }
}