class Number{
    public static void main (String [] args){
        Double n1 = 10.0, n2 = 4.5, n3 = 15.3, largestNumber;

        if(n1 > n2){
            System.out.println("n1 is larger than n2");
            if(n1 > n3){
                System.out.println("n1 is larger than n3");
                largestNumber = n1;
            }
            else{
                System.out.println("n1 is not larger than n3");
                largestNumber = n3;
            }
        }
        else{
            System.out.println("n1 is not larger than n2");
            if(n2 > n3){
                System.out.println("n2 is larger than n3");
                largestNumber = n2;
            }
            else{
                System.out.println("n2 is not larger than n3");
                largestNumber = n3;
            }
        }
        System.out.println("The largest number is "+ largestNumber);
    }
}